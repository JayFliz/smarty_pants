# See https://gist.github.com/maxivak/57a2fb71afeb9efcf771
module ButtonComponents
  def submit_with_cancel(*args, &block)
    template.content_tag :div, :class => "form-group form-actions" do
      template.content_tag :div, :class => "col-sm-offset-3 col-sm-9" do
        if cancel = args.last.delete(:cancel)
          submit(*args, &block) + template.link_to(
            I18n.t("simple_form.buttons.cancel"), cancel, :class => "cancel"
          )
        else
          submit(*args, &block)
        end
      end
    end
  end
end

SimpleForm::FormBuilder.send(:include, ButtonComponents)

